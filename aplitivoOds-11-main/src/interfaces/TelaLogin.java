    private void lg_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lg_btnActionPerformed
       String email = em_lg.getText();
       String senha = new String(psw_lg.getPassword());
       String resultado = bancoDeDados.validarLogin(email, senha);
       System.out.println("Resultado do login: " + resultado);
        JOptionPane.showMessageDialog(this, tradutor.traduzir("Operação realizada com sucesso"));
        Usuario usuario = bancoDeDados.obterUsuarioPorEmail(email);
        UsuarioLogado.setUsuario(usuario);
        TelaInicial tli = new TelaInicial();
        tli.setVisible(true);
        dispose();

    }//GEN-LAST:event_lg_btnActionPerformed

