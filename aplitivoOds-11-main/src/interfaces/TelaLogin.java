    public TelaLogin() {
        // Solicita ao usuário que escolha um idioma
    String[] opcoes = { "Português", "Inglês", "Espanhol" };
    int escolha = JOptionPane.showOptionDialog(null, "Escolha o idioma", "Idioma",
            JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, opcoes, opcoes[0]);

        if (escolha == 0) {
            Idioma.setIdiomaAtual("pt");
        } else if (escolha == 1) {
            Idioma.setIdiomaAtual("en");
        } else if (escolha == 2) {
            Idioma.setIdiomaAtual("es");
        }


        // Inicializa o tradutor e o banco de dados
        tradutor = new Tradutor(Idioma.getIdiomaAtual());
        bancoDeDados = new BancoDeDados();
        initComponents();
        atualizarLabels();
        
        pt_btn.addActionListener(e -> {
            Idioma.setIdiomaAtual("pt");
                    atualizarLabels();
        });

        en_btn.addActionListener(e -> {
            Idioma.setIdiomaAtual("en");
                    atualizarLabels();
        });
         es_btn.addActionListener(e -> {
            Idioma.setIdiomaAtual("es");
                    atualizarLabels();
        });
    }

